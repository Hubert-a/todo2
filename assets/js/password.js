var state = false;
function toggle(){
     if(state){
        document.getElementById('floatingPassword').setAttribute('type', 'password');
        document.getElementById('')
        state=false;
     }
     else{
        document.getElementById('floatingPassword').setAttribute('type', 'text');
        state=true;
     }
}
